    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nom = tfnom.getText().toUpperCase();
            String adresse = tfadresse.getText();
            BigDecimal solde = BigDecimal.valueOf(Double.parseDouble(tfsolde.getText()));
            if (!nom.isEmpty() && !tfsolde.getText().isEmpty()) {
                Client client = new Client();
                client.setNom(nom);
                client.setAdresse(adresse);
                client.setSolde(solde);
                String bt = jButton1.getText();
                if (bt.equalsIgnoreCase("Ajouter")) {
                    clientws.addClient(client);
                } else {
                    client.setId(Integer.parseInt(tfid.getText()));
                    clientws.updateClient(client);
                }
                this.chargementTableau("", "");
                this.effacer();
            } else {
                JOptionPane.showMessageDialog(this, "Veuillez remplir les champs vides");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "démarrer votre serveur");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

