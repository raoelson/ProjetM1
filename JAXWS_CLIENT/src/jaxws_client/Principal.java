    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            if (!tfiduser.getText().isEmpty()) {
                int rep = JOptionPane.showConfirmDialog(this, "Voulez-vous supprimer id =" + tfiduser.getText() + "? ", "Boite de Confirmation", JOptionPane.YES_NO_OPTION);
                if (rep == JOptionPane.YES_OPTION) {
                    clientws.deleteUser(Integer.parseInt(tfiduser.getText()));
                }
                this.chargementUser();
                this.effacerUser();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "démarrer votre serveur");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            String login = tflogin.getText();
            String password = tfpassword.getText();
            if (!login.isEmpty() && !password.isEmpty()) {
                User user = new User();
                user.setLogin(login);
                user.setPassword(password);
                String bt = jButton5.getText();
                if (bt.equalsIgnoreCase("Ajouter")) {
                    clientws.addUser(user);
                } else {
                    user.setId(Integer.parseInt(tfiduser.getText()));
                    clientws.updateUser(user);
                }
                this.chargementUser();
                this.effacerUser();
            } else {
                JOptionPane.showMessageDialog(this, "Veuillez remplir les champs vides");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "démarrer votre serveur");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

