    public void chargementTableau(String cle, String ob) {
        try {
            Iterator it = null;
            dt = new DefaultTableModel();
            dt.addColumn("id");
            dt.addColumn("Nom");
            dt.addColumn("Adresse");
            dt.addColumn("Solde");
            tableau.setModel(dt);
            dt.setRowCount(0);
            if (cle.isEmpty() && ob.isEmpty()) {
                it = clientws.getAllClient().iterator();
            } else {
                it = clientws.searchClient(cle, ob).iterator();
            }

            while (it.hasNext()) {
                Client client = (Client) it.next();
                Object[] affiche = {client.getId(), client.getNom(), client.getAdresse(), client.getSolde()};
                dt.addRow(affiche);
                tableau.setModel(dt);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "démarrer votre serveur");
        }
    }

