    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String rep = JOptionPane.showInputDialog(this, "Entrez votre pseudo", "Authentification", JOptionPane.INFORMATION_MESSAGE);
        if (rep.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Connecté d\'abord");
        } else {

            for (String user : user_liste) {
                if (user.equals(rep)) {
                    nom_connecte = rep;
                }
            }
            if (nom_connecte.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pseudo incorrecte");
            } else {
                jLabel5.setVisible(true);
                user_connecte.setVisible(true);
                user_connecte.setText(nom_connecte);
                jButton3.setEnabled(true);
                jButton1.setEnabled(true);
                jButton2.setEnabled(false);
                jLabel4.setVisible(true);
                Txt_Messages.setEnabled(true);
                this.setTitle("Chat_" + nom_connecte);

                this.message = new ChatMessage();
                this.message.setAction(Action.CONNECT);
                this.message.setName(nom_connecte);
                if (this.socket == null) {
                    this.service = new Client_Service();
                    this.socket = this.service.connect();

                    new Thread(new listenerSocket(this.socket)).start();
                }
                this.service.send(message);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

