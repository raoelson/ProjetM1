    private void refreshOnlines(ChatMessage message) {
        Set<String> names = message.getSetOnlines();
        names.remove(message.getName());
        String[] arrays = (String[]) names.toArray(new String[names.size()]);
        this.list_enligne.setListData(arrays);
        this.list_enligne.setSelectionMode(ListSelectionModel.SINGLE_INTERVAL_SELECTION);
        this.list_enligne.setLayoutOrientation(JList.VERTICAL);
    }

